import { PDFParse } from "pdf-parse";
import {
  generateInterviewReport,
  generatePDF,
} from "../../services/ai.services.js";
import interviewReportModel from "../models/interviewReport.model.js";
import { getTodaysDate } from "../../utils/date.utils.js";

/**
 * @route POST /api/inter/uploadcv
 * @description to get the uploaded resume from the user
 */
const handleFileUploadandGenerateReport = async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({
        status: false,
        message: "the file is not uploaded",
      });
    }
    const resumeFileContents = await new PDFParse({
      data: Uint16Array.from(req.file.buffer),
    }).getText();
    const { profileSummary, jobDescription } = req.body;

    const interviewReportByAI = await generateInterviewReport({
      resume: resumeFileContents.text,
      jobDescription,
      profileSummary,
    });
    const interviewReport = await interviewReportModel.create({
      user: req.user.id,
      resume: resumeFileContents.text,
      jobDescription,
      profileSummary,
      ...interviewReportByAI,
    });

    return res.status(201).json({
      success: true,
      message: "file uploaded succesfully and Created the Report",
      aiResponse: interviewReport,
    });
  } catch (err) {
    console.error("something went wrong in creating the interview report", err);
    return res.status(500).json({
      success: false,
      message:
        "Due to high demand model is experiencing, diffculty processiong your request pleasae try again later.",
    });
  }
};

/**
 * @route POST /api/interview/interviewReportId
 * @description getting the report of the previously generated interview id
 */
const getInterviewReportbyId = async (req, res) => {
  try {
    const { interviewReportId } = req.params;

    const interviewReport =
      await interviewReportModel.findById(interviewReportId);

    if (!interviewReport) {
      return res.status(400).json({
        sucess: false,
        message: "No Report found for the provided Id.",
        interviewReport: {},
      });
    }

    return res.status(200).json({
      success: false,
      message: "Report fetched succesfully",
      interviewReport,
    });
  } catch (err) {
    console.error("Error fetching the report", err);
    return res.status(500).json({
      success: false,
      message: "Something went Wrong.",
    });
  }
};

/**
 * @route GET /api/interview/
 * @description get all the reports generated by the users in the past;
 */
const getAllInterviewReports = async (req, res) => {
  try {
    const interviewReports = await interviewReportModel
      .find({
        user: req.user.id,
      })
      .select(
        "-technicalQuestions -behavioralQuestions -skillGaps -preparationPlan -user -resume -jobDescription -__v",
      )
      .sort({ createdAt: -1 });

    return res.status(200).json({
      success: true,
      message: "Reports fetched",
      reports: interviewReports,
    });
  } catch (err) {
    console.error("error while fetching the reports", err);
    return res.status(500).json({
      success: false,
      message: "Something went Wrong.",
    });
  }
};
/**
 * @route POST /api/generatepdf/:interviewId
 * @description this route will generate the pdf based on the user inputs
 */
const generatePDFFromId = async (req, res) => {
  try {
    const { interviewId } = req.params;

    const foundInterviewReport =
      await interviewReportModel.findById(interviewId);
    if (!foundInterviewReport) {
      return res.status(200).json({
        status: false,
        message: "no report found for the given job.",
      });
    }
    const formattedJobTitle = foundInterviewReport.jobTitle.replace(" ", "_");

    const generatedPdf = await generatePDF(foundInterviewReport);
    const dateStamp = getTodaysDate();
    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      `attachment; filename=${req.user.username}_${formattedJobTitle}_${dateStamp}.pdf`, // Suggests a filename to the browser
    );
    res.setHeader("Content-Length", generatedPdf.length);

    // 2. Send the binary buffer
    return res.end(generatedPdf);
  } catch (error) {
    console.log("error in generating the pdf", error);
    return res.status(500).json({
      success: false,
      message: "Something went Wrong.",
    });
  }
};
export {
  handleFileUploadandGenerateReport,
  getInterviewReportbyId,
  getAllInterviewReports,
  generatePDFFromId,
};
